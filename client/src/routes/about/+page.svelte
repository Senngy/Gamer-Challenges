<div class="about-container">
  <h1>À propos de GamerChallenges</h1>

  <section>
    <h2>Notre projet</h2>
    <p>
      <strong>GamerChallenges</strong> est une plateforme communautaire dédiée aux passionnés de jeux vidéo, permettant de créer, relever et partager des challenges autour de leurs jeux préférés. Ce projet a été imaginé et développé dans le cadre du cursus étudiant à O’Clock, avec pour objectif de favoriser l’échange, la compétition amicale et la créativité au sein de la communauté gaming.
    </p>
  </section>

  <section>
    <h2>Fonctionnalités principales</h2>
    <ul>
      <li>Création et participation à des challenges sur de nombreux jeux vidéo</li>
      <li>Classements et système de votes pour valoriser les meilleures performances</li>
      <li>Gestion de profil joueur et suivi des challenges réalisés</li>
      <li>Navigation intuitive et interface moderne pensée pour les gamers</li>
    </ul>
  </section>

  <section>
    <h2>Nos valeurs</h2>
    <ul>
      <li><strong>Communauté :</strong> Favoriser l’entraide, le partage et la convivialité entre joueurs</li>
      <li><strong>Créativité :</strong> Encourager la création de challenges originaux et stimulants</li>
      <li><strong>Accessibilité :</strong> Proposer une plateforme simple, responsive et ouverte à tous</li>
      <li><strong>Respect :</strong> Garantir un espace sain, inclusif et respectueux des données personnelles</li>
    </ul>
  </section>

  <section>
    <h2>Notre équipe</h2>
    <p>
      GamerChallenges est un projet étudiant porté par <strong>Lamine Boumaza</strong> dans le cadre de la formation O’Clock. Le projet a mobilisé des compétences en développement web, gestion de projet, design et modélisation de données.
    </p>
  </section>

  <section>
    <h2>Technologies & conception</h2>
    <ul>
      <li>Développement web moderne (SvelteKit, JavaScript, CSS)</li>
      <li>Base de données structurée (modélisation MCD/MLD/MPD)</li>
      <li>Charte graphique originale, pensée pour l’univers gaming</li>
      <li>Wireframes et maquettes pour une expérience utilisateur optimale</li>
    </ul>
  </section>

  <section>
    <h2>Contexte & avenir</h2>
    <p>
      Ce site est un <strong>projet étudiant</strong> et un MVP (Minimum Viable Product). Il a vocation à évoluer avec de nouvelles fonctionnalités, telles que les commentaires, les filtres de recherche, ou encore un système de récompenses pour les joueurs les plus actifs.
    </p>
  </section>
</div>

